class Person {
  final String id;
  final String name;
  final String photoUrl;
  final int age;
  final List<String> interests;
  final double distance;
  final int mutualFriends;

  Person({
    required this.id,
    required this.name,
    required this.photoUrl,
    required this.age,
    required this.interests,
    required this.distance,
    required this.mutualFriends,
  });
}
